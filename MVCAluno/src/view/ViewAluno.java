    public void informacao(){
       DefaultTableModel data = new DefaultTableModel();
       data.setNumRows(0);
       data.addColumn("RA");
       data.addColumn("Nome");
       
       for(Aluno a: lstAluno.getList()){
           data.addRow(new Object[]{a.getRa(), a.getNome()});
       }
       tblAlun.setModel(data);
    }
    
