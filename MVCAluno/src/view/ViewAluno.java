    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed

        int ra = Integer.parseInt(this.txtRa.getText());
        String nome = this.txtNome.getText();
        try{
            lstAluno.inserirAluno(ra, nome);
            limparCampos();
            informacao();
            JOptionPane.showMessageDialog(null, "inserido com sucess");
        } catch (SQLException ex){
            JOptionPane.showMessageDialog(null, "não foi possível inseir aluno");
            System.out.println("Erro ao incluir" + ex.toString());
        }
        
       
        
    }//GEN-LAST:event_btnIncluirActionPerformed

    private void btnProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcurarActionPerformed
        try {
            Aluno a = lstAluno.buscaRA(Integer.parseInt(txtProcurar.getText()));
        } catch (NoSuchElementException e) {
            JOptionPane.showMessageDialog(null, "RA não encontrado");
    }//GEN-LAST:event_btnProcurarActionPerformed
    }
    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            lstAluno.removerRA(Integer.parseInt(txtProcurar.getText()));
        } catch (NoSuchElementException e) {
            JOptionPane.showMessageDialog(null, "RA não encontrado");
        } catch (SQLException e){
            
        }
        informacao();
    }//GEN-LAST:event_btnExcluirActionPerformed

